def normalizeRows(x):
    """ Row normalization function """
    # Implement a function that normalizes each row of a matrix to have unit length
    
    ### YOUR CODE HERE
    length = np.sqrt(np.sum(x ** 2, axis=1)).reshape(-1, 1).astype(float)
    x = x / length
    ### END YOUR CODE
    
    return x

